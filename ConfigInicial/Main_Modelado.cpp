 void Inputs(GLFWwindow *window) {
	 //cierra la ventana
	 if (glfwGetKey(window, GLFW_KEY_ESCAPE) == GLFW_PRESS)  //GLFW_RELEASE
		 glfwSetWindowShouldClose(window, true);
	 //mueve la camara en x positivo (derecha)
	 if (glfwGetKey(window, GLFW_KEY_D) == GLFW_PRESS)
		 movX += 0.08f;
	 //mueve la camara en x negativo (derecha)
	 if (glfwGetKey(window, GLFW_KEY_A) == GLFW_PRESS)
		 movX -= 0.08f;
	 //mueve la camara en y positivo (arriba)
	 if (glfwGetKey(window, GLFW_KEY_UP) == GLFW_PRESS)
		 movY += 0.08f;
	 //mueve la camara en y negativo (abajo)
	 if (glfwGetKey(window, GLFW_KEY_DOWN) == GLFW_PRESS)
		 movY -= 0.08f;
	 //mueve la camara en z negativo (atras)
	 if (glfwGetKey(window, GLFW_KEY_W) == GLFW_PRESS)
		 movZ -= 0.08f;
	 //mueve la camara en z positivo (adelante)
	 if (glfwGetKey(window, GLFW_KEY_S) == GLFW_PRESS)
		 movZ += 0.08f;
	 //gira la camara a la derecha en Y
	 if (glfwGetKey(window, GLFW_KEY_RIGHT) == GLFW_PRESS)
		 rot += 0.4f;
	 //gira la camara a la izquierda en Y
	 if (glfwGetKey(window, GLFW_KEY_LEFT) == GLFW_PRESS)
		 rot -= 0.4f;
	 //gira la camara a la derecha en X
	 if (glfwGetKey(window, GLFW_KEY_Q) == GLFW_PRESS)
		 rotX += 0.4f;
	 //gira la camara a la izquierda en X 
	 if (glfwGetKey(window, GLFW_KEY_E) == GLFW_PRESS)
		 rotX -= 0.4f;
	 //gira la camara a la derecha en Z
	 if (glfwGetKey(window, GLFW_KEY_V) == GLFW_PRESS)
		 rotZ += 0.4f;
	 //gira la camara a la izquierda en Z
	 if (glfwGetKey(window, GLFW_KEY_B) == GLFW_PRESS)
		 rotZ -= 0.4f;
 }


